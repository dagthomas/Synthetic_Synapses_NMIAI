WBF_IOU = {wbf_iou}


def wbf_single_image(boxes_list, scores_list, labels_list, img_w, img_h,
                      iou_thr=WBF_IOU, skip_thr=0.001, weights=None):
    """Weighted Boxes Fusion for a single image (pure numpy, no external deps).

    Each input list has one entry per model.
    Boxes are in xyxy pixel coordinates.
    """
    if weights is None:
        weights = [1.0] * len(boxes_list)

    # Normalize to [0, 1]
    all_boxes, all_scores, all_labels, all_weights = [], [], [], []
    for i, (boxes, scores, labels) in enumerate(zip(boxes_list, scores_list, labels_list)):
        if len(boxes) == 0:
            continue
        norm = boxes.copy().astype(np.float64)
        norm[:, [0, 2]] /= img_w
        norm[:, [1, 3]] /= img_h
        np.clip(norm, 0, 1, out=norm)
        for j in range(len(boxes)):
            if scores[j] < skip_thr:
                continue
            all_boxes.append(norm[j])
            all_scores.append(float(scores[j]) * weights[i])
            all_labels.append(int(labels[j]))
            all_weights.append(weights[i])

    if not all_boxes:
        return np.empty((0, 4)), np.empty(0), np.empty(0, dtype=int)

    all_boxes = np.array(all_boxes)
    all_scores = np.array(all_scores)
    all_labels = np.array(all_labels)

    # Sort by score descending
    order = np.argsort(-all_scores)
    all_boxes = all_boxes[order]
    all_scores = all_scores[order]
    all_labels = all_labels[order]

    # Cluster boxes by class and IoU
    clusters = []  # list of (summed_box, total_weight, count, label, max_score)
    used = np.zeros(len(all_boxes), dtype=bool)

    for i in range(len(all_boxes)):
        if used[i]:
            continue
        label = all_labels[i]
        box_i = all_boxes[i]
        cluster_box = box_i * all_scores[i]
        cluster_weight = all_scores[i]
        cluster_count = 1
        max_score = all_scores[i]
        used[i] = True

        for j in range(i + 1, len(all_boxes)):
            if used[j] or all_labels[j] != label:
                continue
            # Compute IoU
            box_j = all_boxes[j]
            xx1 = max(box_i[0], box_j[0])
            yy1 = max(box_i[1], box_j[1])
            xx2 = min(box_i[2], box_j[2])
            yy2 = min(box_i[3], box_j[3])
            inter = max(0, xx2 - xx1) * max(0, yy2 - yy1)
            area_i = (box_i[2] - box_i[0]) * (box_i[3] - box_i[1])
            area_j = (box_j[2] - box_j[0]) * (box_j[3] - box_j[1])
            iou = inter / (area_i + area_j - inter) if (area_i + area_j - inter) > 0 else 0

            if iou > iou_thr:
                cluster_box += all_boxes[j] * all_scores[j]
                cluster_weight += all_scores[j]
                cluster_count += 1
                max_score = max(max_score, all_scores[j])
                used[j] = True
                # Update reference box to cluster average
                box_i = cluster_box / cluster_weight

        avg_box = cluster_box / cluster_weight
        # Score: average boosted by number of models agreeing
        fused_score = max_score * min(cluster_count, len(boxes_list)) / len(boxes_list)
        clusters.append((avg_box, fused_score, label))

    if not clusters:
        return np.empty((0, 4)), np.empty(0), np.empty(0, dtype=int)

    out_boxes = np.array([c[0] for c in clusters])
    out_scores = np.array([c[1] for c in clusters])
    out_labels = np.array([c[2] for c in clusters], dtype=int)

    # Denormalize
    out_boxes[:, [0, 2]] *= img_w
    out_boxes[:, [1, 3]] *= img_h

    return out_boxes, out_scores, out_labels


def run_inference(input_dir, output_path):
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"Device: {{device}}, Models: {{len(MODEL_NAMES)}}")
    start_time = time.time()

    input_dir = Path(input_dir)
    output_path = Path(output_path)

    # Load all models
    models = []
    for name in MODEL_NAMES:
        pt = SCRIPT_DIR / name
        print(f"Loading {{name}}...")
        models.append(YOLO(str(pt)))
    print(f"All models loaded in {{time.time() - start_time:.1f}}s")

    image_files = sorted(
        list(input_dir.glob("*.jpg")) +
        list(input_dir.glob("*.jpeg")) +
        list(input_dir.glob("*.png"))
    )
    print(f"Found {{len(image_files)}} test images")

    predictions = []

    for img_idx, img_path in enumerate(image_files):
        elapsed = time.time() - start_time
        if elapsed > TIMEOUT_SECONDS:
            print(f"WARNING: Timeout ({{elapsed:.0f}}s), stopping at {{img_idx}}/{{len(image_files)}}")
            break

        stem = img_path.stem
        try:
            image_id = int(stem.split("_")[-1])
        except ValueError:
            image_id = img_idx + 1

        # Run all models
        boxes_list, scores_list, labels_list = [], [], []
        img_w, img_h = 1, 1

        for model in models:
            results = model.predict(
                str(img_path), imgsz=IMGSZ, conf=CONF, iou=IOU_NMS,
                max_det=MAX_DET, augment=False, verbose=False, device=device,
            )
            if results and len(results[0].boxes) > 0:
                r = results[0]
                boxes_list.append(r.boxes.xyxy.cpu().numpy())
                scores_list.append(r.boxes.conf.cpu().numpy())
                labels_list.append(r.boxes.cls.cpu().numpy().astype(int))
                img_w = r.orig_img.shape[1]
                img_h = r.orig_img.shape[0]
            else:
                boxes_list.append(np.empty((0, 4)))
                scores_list.append(np.empty(0))
                labels_list.append(np.empty(0, dtype=int))

        # WBF merge
        boxes, scores_arr, labels_arr = wbf_single_image(
            boxes_list, scores_list, labels_list, img_w, img_h
        )

        for i in range(len(boxes)):
            x1, y1, x2, y2 = boxes[i]
            predictions.append({{
                "image_id": image_id,
                "category_id": int(labels_arr[i]),
                "bbox": [round(float(x1), 2), round(float(y1), 2),
                         round(float(x2 - x1), 2), round(float(y2 - y1), 2)],
                "score": round(float(scores_arr[i]), 4),
            }})

        if (img_idx + 1) % 50 == 0:
            elapsed = time.time() - start_time
            print(f"[{{img_idx+1}}/{{len(image_files)}}] {{len(predictions)}} dets, {{elapsed:.0f}}s")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(predictions, f)

    total_time = time.time() - start_time
    print(f"\\nDone! {{len(predictions)}} predictions in {{total_time:.1f}}s")


