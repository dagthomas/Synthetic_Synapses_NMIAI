def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--pick", nargs="*", default=None,
                        help="Manually pick model names (without .pt extension)")
    parser.add_argument("--wbf-iou", type=float, default=0.55,
                        help="WBF IoU threshold for generated run.py")
    parser.add_argument("--output-dir", default=None,
                        help="Export directory (default: ./export)")
    args = parser.parse_args()

    export_dir = Path(args.output_dir) if args.output_dir else EXPORT_DIR
    export_dir.mkdir(parents=True, exist_ok=True)

    # Find weight files
    if args.pick:
        weight_files = [WEIGHTS_DIR / f"{name}.pt" for name in args.pick]
        for wf in weight_files:
            if not wf.exists():
                print(f"ERROR: {wf} not found")
                return
        total_mb = sum(wf.stat().st_size / 1024 / 1024 for wf in weight_files)
    else:
        all_weights = sorted(WEIGHTS_DIR.glob("*.pt")) if WEIGHTS_DIR.exists() else []
        if not all_weights:
            print("No weights found in weights/ directory. Train models first.")
            return
        weight_files, total_mb = select_best_combination(all_weights, WEIGHT_BUDGET_MB, MAX_WEIGHT_FILES)

    print(f"\nSelected {len(weight_files)} models ({total_mb:.1f} MB):")
    for wf in weight_files:
        print(f"  {wf.name}: {wf.stat().st_size / 1024 / 1024:.1f} MB")

    if total_mb > WEIGHT_BUDGET_MB:
        print(f"\nWARNING: Total {total_mb:.1f} MB exceeds budget {WEIGHT_BUDGET_MB} MB!")
        print("Consider using fewer models or ONNX export.")

    # Copy weight files to export dir
    model_names = []
    for wf in weight_files:
        dst = export_dir / wf.name
        shutil.copy2(wf, dst)
        model_names.append(wf.name)
        print(f"  Copied {wf.name} -> {dst}")

    # Generate run.py
    run_py_content = generate_ensemble_run_py(model_names, wbf_iou=args.wbf_iou)
    run_py_path = export_dir / "run.py"
    with open(run_py_path, "w") as f:
        f.write(run_py_content)
    print(f"\nGenerated {run_py_path}")

    # Summary
    total_export_mb = sum(f.stat().st_size for f in export_dir.iterdir()) / 1024 / 1024
    n_py = len(list(export_dir.glob("*.py")))
    n_pt = len(list(export_dir.glob("*.pt")))
    print(f"\n{'='*60}")
    print(f"EXPORT COMPLETE: {export_dir}")
    print(f"  Weight files: {n_pt} / {MAX_WEIGHT_FILES} max")
    print(f"  Python files: {n_py} / 10 max")
    print(f"  Total size: {total_export_mb:.1f} MB / {MAX_ZIP_MB} MB max")
    print(f"\nTo build submission zip:")
    print(f"  cd {export_dir}")
    print(f'  zip -r submission_ensemble.zip . -x ".*"')
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
