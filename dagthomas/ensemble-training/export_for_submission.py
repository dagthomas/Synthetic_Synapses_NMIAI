def strip_and_measure(pt_path):
    """Load checkpoint, strip optimizer, return size in MB."""
    ckpt = torch.load(str(pt_path), map_location="cpu", weights_only=False)
    ckpt["optimizer"] = None
    # Save to temp to measure
    tmp = pt_path.parent / f".tmp_{pt_path.name}"
    torch.save(ckpt, str(tmp))
    size_mb = tmp.stat().st_size / 1024 / 1024
    tmp.unlink()
    return size_mb


def select_best_combination(weight_files, budget_mb, max_files):
    """Select the best combination of models that fits in budget."""
    # Measure sizes
    sizes = {}
    for wf in weight_files:
        sizes[wf.name] = wf.stat().st_size / 1024 / 1024

    print(f"\nAvailable weights ({len(weight_files)}):")
    for wf in weight_files:
        print(f"  {wf.name}: {sizes[wf.name]:.1f} MB")

    # Sort by name to prioritize yolov8x over yolov8l
    sorted_files = sorted(weight_files, key=lambda f: (
        0 if "yolov8x" in f.name else 1,  # prefer x over l
        f.name
    ))

    # Greedy: pick models until budget/count is exhausted
    selected = []
    total_mb = 0
    for wf in sorted_files:
        if len(selected) >= max_files:
            break
        if total_mb + sizes[wf.name] > budget_mb:
            continue
        selected.append(wf)
        total_mb += sizes[wf.name]

    return selected, total_mb


