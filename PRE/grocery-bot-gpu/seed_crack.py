if __name__ == '__main__':
    import argparse
    parser = argparse.ArgumentParser(description='Test seed cracker')
    parser.add_argument('difficulty', default='expert', nargs='?',
                        choices=['easy', 'medium', 'hard', 'expert', 'nightmare'])
    parser.add_argument('seed', type=int, default=42, nargs='?')
    parser.add_argument('--range', type=int, default=10_000_000,
                        help='Search range [0, range)')
    parser.add_argument('--workers', type=int, default=8)
    args = parser.parse_args()

    from game_engine import build_map, generate_all_orders

    ms = build_map(args.difficulty)
    all_orders = generate_all_orders(args.seed, ms, args.difficulty)
    o0 = list(all_orders[0]._required_names)
    o1 = list(all_orders[1]._required_names)

    print(f"Testing crack_seed_fast for {args.difficulty} seed={args.seed}")
    print(f"  Order 0: {o0}")
    print(f"  Order 1: {o1}")

    found = crack_seed_fast(o0, o1, ms, args.difficulty,
                            seed_range=(0, args.range), n_workers=args.workers)
    if found == args.seed:
        print(f"PASS: found seed={found}")
    elif found is not None:
        print(f"FAIL: expected {args.seed}, got {found}")
    else:
        print(f"FAIL: seed not found in [0, {args.range})")
